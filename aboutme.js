// Open About Me Section
function openAboutMe() {
    // Changes the overlay's display property to block, making it visible
    document.getElementById("aboutMeOverlay").style.display = "block";
}

// Close About Me Section
function closeAboutMe() {
    // Changes the overlay's display property to none, hiding it
    document.getElementById("aboutMeOverlay").style.display = "none";
}
